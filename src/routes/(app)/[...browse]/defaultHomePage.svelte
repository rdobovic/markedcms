<h1>Your amazing markdown website</h1>

<p>
    So, you chose to write your website using Markdown language? <b>MarkedCMS</b>
    provides you with simple web interface which you can use to write posts
    and structure them into categories. Once you create your post structure,
    <b>MarkedCMS</b> will generate sidebar menu to navigate your posts and categories.
</p>

<p>
    Here are some steps to get you started:
</p>

<ul>
    <li>
        <b>Write your home page</b><br>
        Each website needs a home page, so login and open your admin dashboard.
        Here you can create normal new post, once you are done with doing so, you
        can navigate to Settings, choose your new post in Home page dropdown and
        click Update.
    </li>
    <li>
        <b>Set site title and footer</b><br>
        While you are on the Settings page, you can also change site title
        and footer.
    </li>
    <li>
        <b>Create some categories</b><br>
        You can create post categories on the New category page in your dashboard.
        To nest categories use Parent category dropdown. If you want you can also
        write category page to describe your category. This text will be displayed
        above post list when you open category page. If you want you can also
        disable post list on the category page.
    </li>
    <li>
        <b>Create posts</b><br>
        Now it's time to write some content. When creating new posts you can
        choose if post will have parent post or it will belong to a category. If
        post has parent post it will be displayed in a dropdown under parent post.
    </li>
    <li>
        <b>Alter your post structure</b><br>
        One final step is to choose order in which your posts and categories will 
        be displayed in the menu. To do this open Post structure page in your
        dashboard. Here you can use arrows to reorder elements and little eye
        icon to make elements visible or invisible in the menu. Once you are
        done editing click Save to update DB.
    </li>

</ul>